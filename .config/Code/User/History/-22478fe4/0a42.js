let x = 4;

switch (x) {
  case 1:
    console.log(`x is equal to 1`);
    break;
  case 2:
    console.log(`x is equal to 2`);
    break;
  case 3:
    console.log(`x is equal to 3`);
    break;
  case 4:
    console.log(`x is equal to 4`);
    break;
  default:
    console.log(`X is not correct number!!!`);
}
